// Get all employees from config (for about.html)
function getAllEmployees() {
    const allEmployees = [];
    for (const [page, employees] of Object.entries(employeeConfig)) {
        allEmployees.push(...employees);
    }
    return allEmployees;
}

// Get employees for a specific page
function getEmployeesForPage(pageName) {
    return employeeConfig[pageName] || [];
}

// Get employees for a specific business (by slogan/business name)
function getEmployeesForBusiness(businessName) {
    const allEmployees = getAllEmployees();
    return allEmployees.filter(emp => emp.slogan === businessName);
}
